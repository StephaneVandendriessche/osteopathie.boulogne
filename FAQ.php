	<h1> Questions fréquentes </h1>

	<p> 
	
		<div id="sub">1. L’ostéopathie est-elle reconnue ?</div>
		
		<p>Oui, depuis le 4 mars 2002 et la loi sur la protection des patients, l’ostéopathie est devenue une profession reconnue.	</p>	
	<hr>

		<div id="sub">2. Faut-il aller voir son médecin et avoir une prescription médicale avant de consulter un ostéopathe ?</div>
		
		<p> Non, l’ostéopathie est une profession paramédicale de première intention et ne nécessite pas de prescription médicale.</p>
	<hr>	
	
		<div id="sub">3. Les consultations sont-elles remboursées ?</div>
		
		<p> La sécurité sociale ne rembourse pas l’ostéopathie, en revanche la grande majorité des mutuelles prend en charge
		une partie voire la totalité du prix de la séance. Contactez votre mutuelle pour plus d’informations.</p>
	<hr>
		
		<div id="sub">4. L’ostéopathe fait-il toujours « craquer » ?</div>
		
		<p> Non, l’ostéopathe dispose de plusieurs techniques lui permettant de s’adapter au motif de consultation, à l’âge du patient et à ses antécédents. Si un patient ne souhaite pas subir de manipulation de « crack », l’ostéopathe a alors à sa disposition un large panel de techniques douces en remplacement.</p>
	<hr>	
	
		<div id="sub">5. Quelle est la différence entre un ostéopathe et un kinésithérapeute ?</div>
		
		<p> Un kinésithérapeute utilise des techniques actives (le patient participe à la technique), ou passives (sans participation du sujet)
		pour permettre au patient de retrouver une fonction qui était altérée grâce au développement ou à la récupération musculaire. 
		Il utilise des techniques de massage et de rééducation et agit sur prescription médicale et donc pas en première intention.</p>
	<hr>
	
		<div id="sub">6. Qu’est-ce qu’un ostéopathe exclusif ?</div>
		
		<p> Il s’agit d’un ostéopathe qui a suivi une formation ostéopathique exclusivement,
		ce n’est donc ni un kinésithérapeute-ostéopathe ni un médecin ostéopathe.</p>
	<hr>	
	
		<div id="sub">7. Faut-il absolument passer des radios avant d’aller voir un Ostéopathe ?</div>
		
		<p> Si vous possédez déjà des examens complémentaires, il peut être utile de les apporter avec vous.
		En revanche dans la majorité des cas il n’est pas nécessaire de passer des examens complémentaires avant une séance d’ostéopathie. 
		En cas de doute n’hésitez pas à contacter votre ostéopathe pour lui demander des conseils.</p>
	<hr>	
	
		<div id="sub">8. Est-ce que l’Ostéopathie fait mal ?</div>
		
		<p> Non ! La règle de la non douleur est fondamentale en ostéopathie, on ne manipule jamais contre la douleur. 
		Les techniques sont douces et indolores, et toujours réalisées avec l’accord du patient.</p>
	<hr>	
	
		<div id="sub">9. Combien de séances faut-il ?</div>
		
		<p> En règle générale, pour une douleur aigue (apparue il y a peu de temps), deux séances suffisent, trois au maximum, pour régler le problème.
		En revanche certaines douleurs chroniques peuvent nécessiter un traitement sur plus long terme. 
		Il est également recommandé de faire une séance d’ostéopathie chaque année par prévention.	</p>
