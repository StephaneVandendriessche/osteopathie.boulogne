			<h1> Pourquoi consulter </h1>
			<p>
				Tout d’abord, il n’est pas nécessaire d’avoir mal pour consulter.<br />
				L’ostéopathie a un rôle préventif et peut permettre d’éviter que des douleurs s’installent chroniquement.<br />
				Ensuite l’ostéopathie a un champ d’action très vaste qu’il est difficile de cerner précisément. <br />
				On peut néanmoins citer une liste non exhaustive.<br />
				<br /> </br>
			</p>

		

		
		<h4>Pour le nourrisson :</h4>


		<ul>	 
			<li>Reflux Gastro-Oesophagien (RGO) </li>
			<li>Coliques, diarrhées, constipations</li>
			<li> Troubles de la digestion</li>
			<li>Troubles du sommeil</li>
			<li>Hyperactivité</li>
			<li>Troubles ORL chroniques (otites, nez qui coule, etc…)</li>
			<li>Torticolis</li>
			<li>Plagiocéphalie (asymétrie de la tête) </li>
		</ul>

		<hr>
		<h4>Pour l'enfant :</h4>

		<ul>
			<li>Déformations de la colonne vertébrale (scoliose, lordose, cyphose)</li>
			<li>Maux de tête</li>
			<li>Maux de dos</li>
			<li>Douleurs articulaires</li>
			<li>Troubles ORL</li>
			<li>Troubles digestifs</li>
			<li>Troubles du comportement</li>
		</ul>
		
		<hr>
		<h4>Pour l'adulte :</h4>
		<ul>

			<li>Stress</li>
			<li>Douleurs articulaires</li>
			<li>Faux mouvements</li>
			<li>Douleurs vertébrales (lumbago, torticolis)</li>
			<li>Maux de tête, céphalées, migraines</li>
			<li>Sciatique</li>
			<li>Troubles ORL</li>
			<li>Troubles digestifs</li>
		</ul>

		<hr>
		<h4>Pour la femme enceinte :</h4>
		<ul>
			<li>Douleurs de dos, du coccyx</li>
			<li>Sciatique, cruralgie</li>
			<li>Nausées, vomissements</li>
			<li>Troubles de la digestion</li>
			<li>Troubles circulatoires</li>
			<li>Insomnies</li>
			<li>Fatigue</li>
			<li>Angoisse et stress</li>
		</ul>
		
		<hr>
		<h4>Pour le sportif : </h4>
		<ul>
			<li>Douleurs articulaires et/ou musculaires</li>
			<li>Tendinites, entorses, déchirures et contractures musculaires</li>
			<li>Pubalgie</li>
			<li>Récupération difficile entre et après les épreuves</li>
			<li>Essoufflement à l'effort </li>
		</ul>
		
		<hr>
		<h4>Pour les séniors : </h4>
		<ul>
			<li>Douleurs articulaires et musculaires</li>
			<li>Arthrose, difficulté de locomotion</li>
			<li>Maux de tête, vertiges</li>
			<li>Troubles du sommeil</li>
			<li>Engourdissement des membres</li>
		</ul>
