	<h1>Presentation de l'ostéopathie</h1>
	<p>
		L’ostéopathie est une profession paramédicale exclusivement manuelle, inventée par Andrew Taylor Still
		à la fin du 19ème siècle.<br />
		Elle repose sur quatre grands principes : la globalité, l’interrelation entre la structure et la fonction, et l’auto-guérison du corps.
		Le principe de globalité indique que la zone douloureuse n’est pas toujours la zone malade.<br />
		Par une palpation précise et affinée,l’ostéopathe recherche des pertes de mobilités articulaires entraînant des douleurs sur un territoire parfois proche, mais parfois éloigné. <br />
		Une fois le diagnostic palpatoire effectué, il appliquera des techniques douces, sans douleurs et non dangereuses pour le patient. 
	</p>
	
	<p>
		L’interrelation entre la structure et la fonction signifie que chaque partie anatomique du corps à un rôle bien précis à accomplir.
		Lorsque la structure s’altère, la fonction ne peut plus se réaliser de façon optimale. Lorsque la structure intestinale se dégrade,
		la digestion devient difficile, quand la structure de l’épaule est atteinte, sa mobilité l’est également.<br />
		L’ostéopathe cherchera donc à veiller à ce que la structure soit la plus harmonique possible 
		afin que des phénomènes douloureux ne se mettent pas en place avec le temps,ou pour libérer des douleurs déjà installées.
	</p>
	
	<p>
		Enfin le principe d’auto-guérison, appelée également homéostasie,
		signifie que le corps humain a en lui tout ce dont il a besoin pour se guérir par lui-même. 
		C’est sa force vitale. Il arrive que cette fonction d’auto-guérison soit déréglée, 
		et que le corps ne trouve plus les solutions par lui-même pour lutter contre la douleur. 
		L’ostéopathe doit alors, par des techniques de réajustement, rééquilibrer les tensions entravant la capacité 
		d’auto-guérison du corps du patient.
	
	</p>
